    <h3><b>Database Kerja Sama Ketahanan Pangan</b></h3> 
    <p class="text-justify">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sesuai dengan peran dan fungsi strategis Badan Ketahanan Pangan serta isu ketahanan pangan yang secara intensif dibahas dalam 
	berbagai forum baik di tingkat nasional maupun global, dengan ini kami menyusun database kerja sama ketahanan pangan. <br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Database online ini berguna dalam mempercepat proses penyediaan data kerja sama ketahanan pangan dan gizi yang berguna dalam penentuan rekomendasi kebijakan 
	terkait kerja sama ketahanan pangan dan gizi. Selain  itu, database ini akan berguna sebagai bank data bagi kerja sama ketahanan pangan dan gizi.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Kerja sama ketahanan pangan yang dilakukan pada tingkat global mencakup kerja sama yang telah dijalin oleh Badan Ketahanan Pangan melalui pertemuan 
	pada forum multilateral dan regional. Partisipasi Badan Ketahanan Pangan pada forum multilateral diantaranya FAO (Food and Agriculture Organization), 
	G20, and OIC (Organization of Islamic Cooperation). Adapun partisipasi pada forum regional mencakup APEC-PPFS (Asia Pasific Economic Partnership),  
	ASEAN (Asociation of South-East Asian Nation), dan ASEAN+3 (Jepang, China dan Korea Selatan). Selain itu, BKP juga turut dilibatkan oleh K/L terkait 
	dalam memberikan masukan pada forum WTO (World Trade Organization) khususnya yang terkait isu pertanian dan pangan.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Selain itu pada tingkat nasional, kerja sama yang dibangun diantaranya mencakup upaya Pengarusutamaan Gender (PUG), 
	perubahan iklim dan pengembangan GIAHS/NIAHS (Globally Important Agriculture Heritage System/Nationally Important Agriculture Heritage System).
	</p> 
